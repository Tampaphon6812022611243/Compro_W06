#include <stdio.h>

int main() {
    int a = 5, b = 2;
    float y = 4.5;
    float x = 3.0, r4;

    // คำนวณค่า r1:
    // a++ * b + (int)y % 3
    // a++ = 5 (a จะกลายเป็น 6 หลังใช้), b = 2, (int)y = 4
    // 5 * 2 + 4 % 3 = 10 + 1 = 11
    printf("int r1 = %d\n", a++ * b + (int)y % 3);

    // คำนวณค่า r2:
    // (a > b) && ((int)x / b < 2)
    // a = 6, b = 2, x = 3.0 => (6 > 2) && (3 / 2 < 2) => 1 && 1 => 1
    printf("int r2 = %d\n", (a > b) && ((int)x / b < 2));

    // คำนวณค่า r3:
    // ++x * y - a / 2
    // x = 3.0 => ++x = 4.0, y = 4.5, a = 6
    // 4.0 * 4.5 - 6 / 2 = 18.0 - 3 = 15.0
    printf("float int r3 = %.2f\n", ++x * y - a / 2);

    // คำนวณค่า r4:
    // (x += 1.5) > y || (b-- > 0)
    // x = 4.0 => x += 1.5 => x = 5.5
    // y = 4.5, b = 2
    // 5.5 > 4.5 = true => r4 = 1.0
    r4 = ((x += 1.5) > y) || (b-- > 0);

    // แสดงค่า r4
    printf("float int r4 = %.2f\n", r4);

    return 0; // คืนค่า 0 เพื่อบอกว่าการทำงานสำเร็จ
}
