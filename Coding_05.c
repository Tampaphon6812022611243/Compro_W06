#include <stdio.h>

int main() {
    int a, b, c, d, e, f, g;

    // คำนวณค่า a: -2 + 5 * 2 => -2 + 10 = 8
    printf("A = %d\n", a = -2 + 5 * 2);

    // คำนวณค่า b: 10 / 2 * 3 => 5 * 3 = 15
    printf("B = %d\n", b = 10 / 2 * 3);

    // คำนวณค่า c: 6 / 2 + 3 * (4 % 2) => 3 + 3 * 0 = 3
    printf("C = %d\n", c = 6 / 2 + 3 * (4 % 2));

    // คำนวณค่า d: (5 + 2) * 15 % 4 => 7 * 15 % 4 = 105 % 4 = 1
    printf("D = %d\n", d = (5 + 2) * 15 % 4);

    // คำนวณค่า e: 6 + 2 * 2 - 6 / 2 => 6 + 4 - 3 = 7
    printf("E = %d\n", e = 6 + 2 * 2 - 6 / 2);

    // คำนวณค่า f: 5 + 3 * 2 - 8 / 4 + (6 % 5)
    // => 5 + 6 - 2 + 1 = 10
    printf("F = %d\n", f = 5 + 3 * 2 - 8 / 4 + (6 % 5));

    // คำนวณค่า g: (4 + 3) * 2 - 10 / (2 + 3)
    // => 7 * 2 - 10 / 5 = 14 - 2 = 12
    printf("G = %d\n", g = (4 + 3) * 2 - 10 / (2 + 3));

    return 0;
}
